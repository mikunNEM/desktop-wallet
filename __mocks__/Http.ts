const parseQueryParams = (queryString): Record<string, string> => {
    const query = {};
    queryString.split('&').forEach((pair) => {
        const pairArr = pair.split('=');
        query[pairArr[0]] = pairArr[1] || '';
    });
    return query;
};

export const getHandlers = (responses: HttpResponse[]) => {
    return responses.map((httpResponse) => {
        const method = httpResponse.method;
        const restMethod = rest[method];
        const body = httpResponse.body;
        const path = httpResponse.path;
        const origin = httpResponse.origin;
        const status = httpResponse.status;
        return restMethod(origin + path, (req, res, ctx) => {
            if (httpResponse.queryParams) {
                Object.keys(httpResponse.queryParams).forEach((queryParamKey) => {
                    const paramsMap = parseQueryParams(queryParamKey);
                    if (Object.keys(paramsMap).every((param) => req.url.searchParams.get(param) === paramsMap[param])) {
                        return res(ctx.status(status), ctx.json(httpResponse.queryParams[queryParamKey]));
                    }
                });
            }
            return res(ctx.status(status), ctx.json(typeof body === 'function' ? body() : body));
        });
    });
};
